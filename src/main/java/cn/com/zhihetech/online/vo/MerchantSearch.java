public class MerchantSearch {

    private String merchName;
    private String merchTell;
    private String examinState;
    private String permit;

    public String getMerchName() {
        return merchName;
    }

    public void setMerchName(String merchName) {
        this.merchName = merchName;
    }

    public String getMerchTell() {
        return merchTell;
    }

    public void setMerchTell(String merchTell) {
        this.merchTell = merchTell;
    }

    public String getExaminState() {
        return examinState;
    }

    public void setExaminState(String examinState) {
        this.examinState = examinState;
    }

    public String getPermit() {
        return permit;
    }

    public void setPermit(String permit) {
        this.permit = permit;
    }
}
